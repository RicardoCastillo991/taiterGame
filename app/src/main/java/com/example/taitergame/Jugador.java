package com.example.taitergame;

public class Jugador {
    String nombre;
    int basura = 0;
    int oxigeno = 10;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getBasura() {
        return basura;
    }

    public void setBasura(int basura) {
        this.basura = basura;
    }

    public int getOxigeno() {
        return oxigeno;
    }

    public void setOxigeno(int oxigeno) {
        this.oxigeno = oxigeno;
    }




}
